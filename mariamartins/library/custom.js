$(window).scroll(function() {    
    var scroll = $(window).scrollTop();

    if (scroll >= 100) {
        $(".hideLogo").addClass("showLogo");
        $("#mainNav").addClass("navbar-shrink");
    } else {
        $(".hideLogo").removeClass("showLogo");
        $("#mainNav").removeClass("navbar-shrink");
    }
});
